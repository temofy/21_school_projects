#include "libft.h"

int		ft_strposchr(char *s, int c)
{
	size_t	i;

	i = 0;
	while (i < (ft_strlen(s)))
	{
		if (s[i] == (char)c)
			return (i);
		i++;
	}
	return (-1);
}
